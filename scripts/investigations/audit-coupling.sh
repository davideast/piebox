| \`SessionEvent\` kinds the playground handles                | $(grep -cE "case '(turn_started|text|thinking|tool_started|tool_finished|turn_completed|error|completed)'" examples/browser/src/agent/useAgentLoop.ts) |

EOF
}

emit_implications() {
  cat <<EOF
## Implications for the refactor (rough — to be revised against C/G)

1. **Layer 2 must define its own operation types.** \`src/operations/\`
   currently can't be lifted into a protocol-neutral surface because
   its types come from \`@earendil-works/pi-coding-agent\`. The refactor
   has to introduce piebox-native \`ReadOperations\` /
   \`WriteOperations\` / etc. type aliases — then both SDKs adapt.

2. **The agent driver wraps two SDKs, not one.** A \`@piebox/driver-agent\`
   package would have to either (a) pick one SDK and have the other
   adapt to it, (b) ship sub-drivers for both, or (c) define its own
   agent-driver-internal types and have both SDKs adapt. Option (c)
   is consistent with the Layer 2 principle.

3. **\`ToolContext\` carries fields the tools don't use.** The
   browser playground's tools never read \`ctx.workspace\` or
   \`ctx.runtime\` — only \`ctx.signal\`. The piebox-native tool
   descriptor can be much leaner: \`(args, sandbox, signal) =>
   PieboxResult<T>\`.

4. **\`SessionEvent\` has dead branches in the playground.** The
   playground does not consume \`strategy_event\`, \`workspace_changed\`,
   or \`runtime_changed\`. If MCP and CLI drivers also don't, those
   shapes are pure agent-driver concern and never need to exist in
   Layer 2.

5. **Scope of the refactor (rough):** 16 \`src/\` files touch the
   server-side SDK; 2 \`examples/browser/src/\` files touch the
   browser-side SDK. The browser surface is smaller and likely
   cheaper to refactor first.

EOF
}

emit_header
emit_finding_two_sdks
emit_sdk_inventory "@earendil-works/pi-coding-agent" "@earendil-works/pi-coding-agent"
emit_sdk_inventory "@inbrowser/agent" "@inbrowser/agent"
emit_type_leaks_pi_coding_agent
emit_browser_surface
emit_tool_inventory
emit_session_event_consumers
emit_summary_counts
emit_implications
